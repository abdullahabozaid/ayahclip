// Sync local "aesthetic" background videos into the app.
//
// Usage:  node scripts/sync-aesthetic-videos.mjs ["/path/to/source/folder"]
//
// Copies every video from the source folder (default: your Downloads
// "Aesthetic videos bacgkround" folder) into public/videos/aesthetic/ and
// regenerates src/lib/aesthetic-videos.ts. Drop new clips in the source
// folder and re-run this any time.

import {
  readdirSync,
  copyFileSync,
  mkdirSync,
  writeFileSync,
  existsSync,
  statSync,
} from "fs";
import { join, extname } from "path";
import { execFileSync } from "child_process";

const SRC =
  process.argv[2] || "/Users/abzz/Downloads/Aesthetic videos bacgkround";
const DEST_DIR = "public/videos/aesthetic";
const MANIFEST = "src/lib/aesthetic-videos.ts";
const VIDEO_EXT = [".mp4", ".webm", ".mov"];

function probeDuration(file) {
  try {
    // execFileSync with an args array — no shell, so filenames can't inject.
    const out = execFileSync(
      "ffprobe",
      ["-v", "error", "-show_entries", "format=duration", "-of", "csv=p=0", file],
      { encoding: "utf8" }
    ).trim();
    const d = parseFloat(out);
    return Number.isFinite(d) ? Math.round(d) : null;
  } catch {
    return null;
  }
}

mkdirSync(DEST_DIR, { recursive: true });

if (!existsSync(SRC)) {
  console.error(`Source folder not found: ${SRC}`);
  process.exit(1);
}

const files = readdirSync(SRC)
  .filter((f) => VIDEO_EXT.includes(extname(f).toLowerCase()))
  .filter((f) => statSync(join(SRC, f)).isFile())
  .sort();

const entries = files.map((f, i) => {
  const ext = extname(f).toLowerCase();
  const safe = `aesthetic-${i + 1}${ext}`;
  copyFileSync(join(SRC, f), join(DEST_DIR, safe));
  const duration = probeDuration(join(SRC, f));
  if (ext === ".mov") {
    console.warn(
      `  ⚠ ${f} is .mov — may not play in browsers; consider converting to .mp4 (H.264).`
    );
  }
  return {
    id: `aesthetic-${i + 1}`,
    name: `Aesthetic ${i + 1}`,
    file: `/videos/aesthetic/${safe}`,
    duration,
  };
});

const ts = `// AUTO-GENERATED by scripts/sync-aesthetic-videos.mjs — do not edit by hand.
export interface AestheticVideo {
  id: string;
  name: string;
  file: string;
  /** Duration in whole seconds, probed at sync time (null if unknown). */
  duration: number | null;
}

export const AESTHETIC_VIDEOS: AestheticVideo[] = ${JSON.stringify(entries, null, 2)};
`;

writeFileSync(MANIFEST, ts);
console.log(
  `Synced ${entries.length} video(s) → ${DEST_DIR}\nWrote manifest → ${MANIFEST}`
);
